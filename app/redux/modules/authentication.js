import { getAccessToken, authWithToken, updateUser } from '~/api/auth'

const AUTHENTICATING = 'AUTHENTICATING'
const NOT_AUTHED = 'NOT_AUTHED'
const IS_AUTHED = 'IS_AUTHED'

function authenticating () {
  return {
    type: AUTHENTICATING
  }
}

function notAuthed () {
  return {
    type: NOT_AUTHED
  }
}

function isAuthed (uid) {
  return {
    type: IS_AUTHED,
    uid
  }
}

export function handleAuthWithFirebase () {
  return function (dispatch, getState) {
    dispatch(authenticating())
    return getAccessToken()
      .then(({accessToken}) => {
        console.warn('accessToken: ', accessToken)
        authWithToken(accessToken)
      })
      .catch((error) => console.warn('Error in handleAuthWithFirebase: ', error.message))
  }
}

export function onAuthChange (user) {
  return function (dispatch) {
    if (!user) {
      dispatch(notAuthed())
    } else {
      // update user in firebase database
      const { uid, displayName, photoURL } = user
      updateUser({
        uid,
        displayName,
        photoURL
      }).then(() => dispatch(isAuthed(uid)))
    }
  }
}

const initialState = {
  isAuthed: false,
  isAuthenticating: true,
  authedId: ''
}

export default function authentication (state = initialState, action) {
  switch (action.type) {
    case AUTHENTICATING :
      return {
        ...state,
        isAuthenticating: true
      }
    case NOT_AUTHED :
      return {
        isAuthenticating: false,
        isAuthed: false,
        authedId: ''
      }
    case IS_AUTHED :
      return {
        isAuthed: true,
        isAuthenticating: false,
        authedId: action.uid
      }
    default :
      return state
  }
}